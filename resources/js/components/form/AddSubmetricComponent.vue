<template>
    <v-app>
        <v-card class="m-4">
            <v-data-table
                :headers="headers"
                :items="submetrics"
                sort-by="submetric_name"
                class="elevation-1"
            >
                <template v-slot:top>
                    <v-toolbar flat color="white">
                        <v-toolbar-title>Add Sub-metrics</v-toolbar-title>
                        <v-divider class="mx-4" inset vertical></v-divider>
                        <v-spacer></v-spacer>
                        <v-dialog v-model="dialog" max-width="800px">
                            <template v-slot:activator="{ on }">
                                <v-btn
                                    color="#F4D03F"
                                    dark
                                    class="mb-2"
                                    v-on="on"
                                    >Add Sub-Metrics
                                </v-btn>
                            </template>

                            <v-card>
                                <v-card-title>
                                    <span class="headline">{{
                                        formTitle
                                    }}</span>
                                </v-card-title>

                                <v-card-text>
                                    <v-container>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="Sub-Metric Name"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="Purpose of the metrics"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="Method of application"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="Measurement, formula and data element computation"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="Metric scale type"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="Measure type"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="Input to measurement"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="ISO/IEC 12207 SLCP Reference"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="Target audience"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-divider></v-divider>
                                        <v-text-field
                                            v-model="editedItem.submetric_name"
                                            label="Question"
                                            persistent-hint
                                            outlined
                                        ></v-text-field>
                                        <v-row>
                                            <v-col>
                                                <v-container
                                                    id="dropdown-example-3"
                                                >
                                                    <v-overflow-btn
                                                        class="my-2"
                                                        :items="dropdown_edit"
                                                        label="Type of question"
                                                        counter
                                                        item-value="text"
                                                    ></v-overflow-btn>
                                                </v-container>
                                            </v-col>
                                            <v-col>
                                                <v-container
                                                    id="dropdown-example-3"
                                                >
                                                    <v-overflow-btn
                                                        class="my-2"
                                                        :items="dropdown_edit"
                                                        label="Level"
                                                        counter
                                                        item-value="text"
                                                    >
                                                    </v-overflow-btn>
                                                </v-container>
                                            </v-col>
                                        </v-row>
                                    </v-container>
                                </v-card-text>

                                <v-card-actions>
                                    <v-spacer></v-spacer>

                                    <v-btn color="blue darken-1" text
                                        >Save</v-btn
                                    >

                                    <v-btn color="#CD4D4D" text @click="close"
                                        >Cancel</v-btn
                                    >
                                </v-card-actions>
                            </v-card>
                        </v-dialog>
                    </v-toolbar>
                </template>
                <template v-slot:item.actions="{ item }">
                    <v-btn
                        class="m-2"
                        outlined
                        color="teal"
                        href="/addmetricdetail/"
                    >
                        <v-icon small class="mr-2">
                            mdi-view-grid-outline
                        </v-icon>
                        view
                    </v-btn>
                    <v-btn
                        class="m-2"
                        small
                        outlined
                        fab
                        color="indigo"
                        @click="editItem(item)"
                    >
                        <v-icon> mdi-pencil</v-icon>
                    </v-btn>

                    <v-btn
                        class="m-2"
                        small
                        outlined
                        fab
                        color="red"
                        @click="deleteItem(item)"
                    >
                        <v-icon> mdi-delete </v-icon>
                    </v-btn>
                </template>
                <template v-slot:no-data>
                    <v-btn color="primary" @click="initialize">Reset</v-btn>
                </template>
            </v-data-table>

            <v-row justify="center" class="m-3">
                <v-btn class="m-2" outlined color="teal">
                    Save
                </v-btn>
                <v-btn class="m-2" outlined color="grey" href="/addmodel/">
                    Back
                </v-btn>
            </v-row>
        </v-card>
    </v-app>
</template>

<script>
export default {
    data: () => ({
        dialog: false,
        headers: [
            {
                text: "Metric ID",
                align: "center",
                sortable: false,
                value: "id"
            },
            {
                text: "Sub-Metric Name",
                value: "submetric_name",
                align: "center"
            },
            {
                text: "Actions",
                value: "actions",
                sortable: false,
                align: "center"
            }
        ],
        submetrics: [],
        editedIndex: -1,
        editedItem: {
            id: "",
            submetric_name: ""
        },
        defaultItem: {
            id: "",
            submetric_name: 0
        },
        dropdown_edit: [
            { text: "5" },
            { text: "4" },
            { text: "3" },
            { text: "2" },
            { text: "1" }
        ]
    }),

    computed: {
        formTitle() {
            return this.editedIndex === -1 ? "New Item" : "Edit Item";
        }
    },

    watch: {
        dialog(val) {
            val || this.close();
        }
    },

    created() {
        this.initialize();
    },

    methods: {
        initialize() {
            this.submetrics = [
                {
                    id: "1",
                    submetric_name: "testsubmetrics"
                },
                {
                    id: "2",
                    submetric_name: "testsubmetrics2"
                },
                {
                    id: "3",
                    submetric_name: "testsubmetrics3"
                }
            ];
        },

        editItem(item) {
            this.editedIndex = this.submetrics.indexOf(item);
            this.editedItem = Object.assign({}, item);
            this.dialog = true;
        },

        deleteItem(item) {
            const index = this.submetrics.indexOf(item);
            confirm("Are you sure you want to delete this Model?") &&
                this.submetrics.splice(index, 1);
        },

        close() {
            this.dialog = false;
            setTimeout(() => {
                this.editedItem = Object.assign({}, this.defaultItem);
                this.editedIndex = -1;
            }, 300);
        },

        save() {
            if (this.editedIndex > -1) {
                Object.assign(
                    this.submetrics[this.editedIndex],
                    this.editedItem
                );
            } else {
                this.submetrics.push(this.editedItem);
            }
            this.close();
        }
    }
};
</script>
